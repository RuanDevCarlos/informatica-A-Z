# Ordenar valores em uma lista
numeros = [7, 15, 9, 1, 2, 3, 8, 7]
print(f'Lista de Números: {numeros}')
numeros.sort() # Ordena os números em ordem crescente
print(f'Em ordem crescente fica: {numeros}')

numeros.sort(reverse = True) # Ordena os números em ordem decrescente
print(f'Lista em ordem decrescente fica: {numeros}')